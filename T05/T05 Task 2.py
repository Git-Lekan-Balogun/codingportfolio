# replace.py Simply we're removing exclamations to make a sentence more legible, we do so using the replace function
# to replace the spam like exclamation marks

font_sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog!."
font_sentence = font_sentence.replace("!", " ")
print(font_sentence)
print(font_sentence.upper())
print(font_sentence[::-1])
